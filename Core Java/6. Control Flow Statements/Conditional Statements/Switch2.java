class Switch2
{
	public static void main(String[] args) 
	{
		String str = "Mahesh";

		switch(str)
		{
			case "Mahesh":
			{
				System.out.println("From case-1");
				break;
			}
			case "Tejus":
			{
				System.out.println("From case-2");
				break;
			}
			case "Ayush":
			{
				System.out.println("From case-3");
				break;
			}
			default:	
			{
				System.out.println("From default");
			}
		}
	}
}
