class Switch3
{
	public static void main(String[] args) 
	{
		char vowel = 'u';

		switch(vowel)
		{
			case 'a':
			{
				System.out.println("From case-a");
				break;
			}
			case 'e':
			{
				System.out.println("From case-e");
				break;
			}
			case 'i':
			{
				System.out.println("From case-i");
				break;
			}
			case 'o':
			{
				System.out.println("From case-o");
				break;
			}
			case 'u':
			{
				System.out.println("From case-u");
				// break;
			}
			default:	
			{
				System.out.println("From default");
			}
		}
	}
}
